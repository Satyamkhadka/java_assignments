class ArrayWorks {

    public static void main(String[] args) {

        int input[] = { 84, 28, 56, 82, 19, 11, 49 };

        // processing
        for (int i = 0; i < input.length; i++) {
            input[i] += 2;

        }

        // output
        for (int i = 0; i < input.length; i++) {
            System.out.print(input[i] + " ");

        }

    }
}